package com.sx.pojo;

public class Cart {

    private int cartid;
    private int pid;
    private int uid;
    private int count;
    
    public int getCartid() {
        return cartid;
    }
    public void setCartid(int cartid) {
        this.cartid = cartid;
    }
    public int getPid() {
        return pid;
    }
    public void setPid(int pid) {
        this.pid = pid;
    }
    public int getUid() {
        return uid;
    }
    public void setUid(int uid) {
        this.uid = uid;
    }
    public int getCount() {
        return count;
    }
    public void setCount(int count) {
        this.count = count;
    }
}
